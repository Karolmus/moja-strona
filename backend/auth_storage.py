def parent_access_by_token(token):
    token_hash = hash_parent_access_token(token)
    row = execute(
        """
        SELECT
            p.id AS access_id,
            p.user_id AS access_user_id,
            p.label AS access_label,
            p.is_active AS access_is_active,
            p.created_at AS access_created_at,
            p.last_used_at AS access_last_used_at,
            p.revoked_at AS access_revoked_at,
            u.id AS student_id,
            u.email AS student_email,
            u.display_name AS student_display_name,
            u.role AS student_role,
            u.level AS student_level,
            u.is_active AS student_is_active,
            u.created_at AS student_created_at,
            u.last_login_at AS student_last_login_at
        FROM parent_access_tokens p
        JOIN users u ON u.id = p.user_id
        WHERE p.token_hash = ?
            AND p.is_active = ?
            AND u.role = 'student'
        LIMIT 1
        """,
        (token_hash, db_bool(True)),
    ).fetchone()

    if not row:
        return None

    item = dict(row)
    student = {
        "id": item["student_id"],
        "email": item["student_email"],
        "display_name": item["student_display_name"],
        "role": item["student_role"],
        "level": item["student_level"],
        "is_active": bool(item["student_is_active"]),
        "created_at": item["student_created_at"],
        "last_login_at": item["student_last_login_at"],
    }
    access = {
        "id": item["access_id"],
        "user_id": item["access_user_id"],
        "label": item["access_label"],
        "is_active": bool(item["access_is_active"]),
        "created_at": item["access_created_at"],
        "last_used_at": item["access_last_used_at"],
        "revoked_at": item["access_revoked_at"],
    }

    return {
        "access": access,
        "student": student,
    }


