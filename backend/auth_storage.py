def speed_training_result_to_dict(row, viewer_user_id=None):
    item = row_to_dict(row)

    if not item:
        return None

    for key in ("id", "user_id", "round_seconds", "correct_count", "mistake_count", "best_streak"):
        if key in item and item[key] is not None:
            item[key] = int(item[key])

    if "average_time_seconds" in item and item["average_time_seconds"] is not None:
        item["average_time_seconds"] = round(float(item["average_time_seconds"]), 3)

    if "student_is_active" in item:
        item["student_is_active"] = bool(item["student_is_active"])

    if viewer_user_id is not None:
        item["is_current_user"] = item.get("user_id") == viewer_user_id

    item.pop("user_id", None)

    return item


