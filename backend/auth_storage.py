def speed_training_leaderboard(filters=None, limit=10, viewer_user_id=None):
    filters = filters or {}
    where = ["u.role = 'student'", "u.is_active = ?"]
    values = [db_bool(True)]
    filter_columns = {
        "level": "r.level",
        "topic": "r.topic",
        "difficulty": "r.difficulty",
        "round_seconds": "r.round_seconds",
    }

    for key, column in filter_columns.items():
        value = filters.get(key)

        if value in (None, ""):
            continue

        if key == "round_seconds":
            value = duration_seconds(value)

        where.append(f"{column} = ?")
        values.append(value)

    period = str(filters.get("period") or "").strip().lower()

    if period in {"week", "weekly"}:
        week_start = datetime.now(timezone.utc) - timedelta(days=7)

        where.append("r.created_at >= ?")
        values.append(week_start.isoformat())

    values.append(bounded_int(limit, 1, 50))
    rows = execute(
        f"""
        WITH ranked AS (
            SELECT
                r.id,
                r.user_id,
                u.display_name,
                u.level AS student_level,
                u.is_active AS student_is_active,
                r.level,
                r.topic,
                r.difficulty,
                r.round_seconds,
                r.correct_count,
                r.mistake_count,
                r.best_streak,
                r.average_time_seconds,
                r.created_at,
                ROW_NUMBER() OVER (
                    PARTITION BY r.user_id
                    ORDER BY
                        r.correct_count DESC,
                        r.mistake_count ASC,
                        r.average_time_seconds ASC,
                        r.best_streak DESC,
                        r.created_at DESC
                ) AS row_rank
            FROM speed_training_results r
            JOIN users u ON u.id = r.user_id
            WHERE {" AND ".join(where)}
        )
        SELECT *
        FROM ranked
        WHERE row_rank = 1
        ORDER BY
            correct_count DESC,
            mistake_count ASC,
            average_time_seconds ASC,
            best_streak DESC,
            created_at DESC
        LIMIT ?
        """,
        tuple(values),
    ).fetchall()

    leaderboard = []

    for index, row in enumerate(rows, start=1):
        item = speed_training_result_to_dict(row, viewer_user_id=viewer_user_id)
        item["rank"] = index
        item.pop("row_rank", None)
        leaderboard.append(item)

    return leaderboard


