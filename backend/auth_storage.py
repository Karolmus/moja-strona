def list_students():
    rows = execute(
        """
        SELECT
            u.id,
            u.email,
            u.display_name,
            u.role,
            u.level,
            u.is_active,
            u.created_at,
            u.last_login_at,
            COUNT(p.id) AS attempts,
            COALESCE(SUM(CASE WHEN p.result = 'good' THEN 1 ELSE 0 END), 0) AS good_count,
            COALESCE(SUM(CASE WHEN p.result = 'medium' THEN 1 ELSE 0 END), 0) AS medium_count,
            COALESCE(SUM(CASE WHEN p.result = 'bad' THEN 1 ELSE 0 END), 0) AS bad_count,
            COALESCE(SUM(COALESCE(p.duration_seconds, 0)), 0) AS total_duration_seconds,
            COALESCE(r.review_count, 0) AS review_count,
            MAX(p.created_at) AS last_activity_at
        FROM users u
        LEFT JOIN task_progress p ON p.user_id = u.id
        LEFT JOIN (
            SELECT user_id, COUNT(*) AS review_count
            FROM task_review_items
            WHERE is_resolved = ?
            GROUP BY user_id
        ) r ON r.user_id = u.id
        WHERE u.role = 'student'
        GROUP BY u.id, u.email, u.display_name, u.role, u.level, u.is_active, u.created_at, u.last_login_at, r.review_count
        ORDER BY u.created_at DESC
        """,
        (db_bool(False),),
    ).fetchall()

    students = []

    for row in rows:
        student = dict(row)
        student["is_active"] = bool(student["is_active"])
        student["accuracy"] = round((student["good_count"] / student["attempts"]) * 100, 1) if student["attempts"] else 0
        student["average_duration_seconds"] = round(student["total_duration_seconds"] / student["attempts"]) if student["attempts"] else 0
        students.append(student)

    return students


def update_student(user_id, fields):
    allowed = {
        "display_name": "display_name",
        "level": "level",
        "is_active": "is_active",
    }
    assignments = []
    values = []

    for key, column in allowed.items():
        if key not in fields:
            continue

        value = fields[key]

        if key == "is_active":
            value = db_bool(value)

        assignments.append(f"{column} = ?")
        values.append(value)

    if not assignments:
        return row_to_dict(
            execute("SELECT * FROM users WHERE id = ? AND role = 'student'", (user_id,)).fetchone()
        )

    values.append(user_id)
    db = get_db()
    cursor = db.execute(
        prepare_sql(f"UPDATE users SET {', '.join(assignments)} WHERE id = ? AND role = 'student'"),
        values,
    )
    db.commit()

    if cursor.rowcount == 0:
        return None

    return row_to_dict(
        execute("SELECT * FROM users WHERE id = ? AND role = 'student'", (user_id,)).fetchone()
    )


